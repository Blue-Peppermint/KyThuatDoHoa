    private void btnPlay_StopItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_btnPlay_StopItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            //System.out.println("Dang bat len");
            drawing2D = true;
            new MyThread(paint2D).start();
        } else if (evt.getStateChange() == ItemEvent.DESELECTED) {
            drawing2D = false;
        }
    }//GEN-LAST:event_btnPlay_StopItemStateChanged

