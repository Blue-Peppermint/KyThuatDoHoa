    private void btn2DActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn2DActionPerformed
        // ko con trang thai ve 3D nua
        this.draw3D = false;
        drawing2D = true;
        // thiet lap cac nut co the truy cap
        btn3D.setEnabled(true);
        btn2D.setEnabled(false);
        //btnPlay_Stop.setEnabled(true);
                btnSphere.setEnabled(false);
        btnRectPrism.setEnabled(false);
        btnClear3D.setEnabled(false);
        // reset drawPane
        drawPane.removeAll();
        paint2D = new Paint_2D(this, drawPane);
        drawPane.add(paint2D);
        drawPane.validate();
        // chay animation
        btnPlay_Stop.setSelected(true);
        panelInfo2D.setVisible(true);
    }//GEN-LAST:event_btn2DActionPerformed

    private void btn3DActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn3DActionPerformed
        // ko con trang thai ve 2D nua
        this.drawing2D = false;
        this.draw3D = true;
        // thiet lap cac nut co the truy cap
        btn2D.setEnabled(true);
        btn3D.setEnabled(false);
        //btnPlay_Stop.setEnabled(false);
        btnSphere.setEnabled(true);
        btnRectPrism.setEnabled(true);
        btnClear3D.setEnabled(true);
        // reset drawPane
        drawPane.removeAll();
        paint3D = new Paint_3D(drawPane);
        drawPane.add(paint3D);
        drawPane.repaint();
        drawPane.validate();
        // chay animation, render Draw Paint
        btnPlay_Stop.setSelected(false);
        panelInfo2D.setVisible(false);
    }//GEN-LAST:event_btn3DActionPerformed

